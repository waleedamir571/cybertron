<script>
  document.addEventListener("DOMContentLoaded", function () {
    const section = document.getElementById("hero-section");

    const observer = new IntersectionObserver(
      function (entries, observer) {
        entries.forEach(function (entry) {
          if (entry.isIntersecting) {
            // Delay 1 second (1000 ms) before adding class
            setTimeout(() => {
              section.classList.add("expanded");
            }, 2000); // yahan ap apni marzi ka time (ms me) set karen
            observer.disconnect(); // Only run once
          }
        });
      },
      { threshold: 0.5 }
    );

    observer.observe(section);
  });
</script>


<script>
  const loopContainer = document.querySelector('.loop-container');
  const loopContent = document.getElementById('loopContent');

  // set height to 40rem
  loopContainer.style.height = "40rem";

  // Clone the content for seamless loop
  const clone = loopContent.cloneNode(true);
  loopContent.appendChild(clone);

  let scrollPosition = 0;
  const scrollSpeed = 0.2; // pixels per frame

  function scrollLoop() {
    scrollPosition += scrollSpeed;
    if (scrollPosition >= loopContent.scrollHeight / 2) {
      scrollPosition = 0;
    }
    loopContainer.scrollTop = scrollPosition;
    requestAnimationFrame(scrollLoop);
  }

  scrollLoop();
</script>



